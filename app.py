# ── Load model ────────────────────────────────────────────────────────────────
@st.cache_resource
def load_model():
    with open('model/model.pkl', 'rb') as f:
        model = pickle.load(f)
    with open('model/encoders.pkl', 'rb') as f:
        encs = pickle.load(f)
    with open('model/meta.json') as f:
        meta = json.load(f)
    return model, encs, meta

try:
    model, encs, meta = load_model()
    le_bat, le_bowl, le_venue, le_toss = (
        encs['bat'], encs['bowl'], encs['venue'], encs['toss']
    )
    TEAMS  = meta['teams']
    VENUES = meta['venues']
    MAE    = meta['mae']
    FEATURES = meta['features']
except FileNotFoundError:
    st.error("⚠️ Model files not found. Run the Jupyter notebook first to generate `model/`.")
    st.stop()


# ── Predict function ──────────────────────────────────────────────────────────
def predict(batting_team, bowling_team, venue, current_over,
            current_runs, current_wickets, innings, month, toss_decision):
    bat_enc   = le_bat.transform([batting_team])[0]
    bowl_enc  = le_bowl.transform([bowling_team])[0]
    venue_enc = le_venue.transform([venue])[0]
    toss_enc  = le_toss.transform([toss_decision])[0]

    overs_remaining   = 20 - current_over - 1
    run_rate          = current_runs / max(current_over + 1, 1)
    wickets_remaining = 10 - current_wickets
    batting_first     = int(innings == 1)
    projected_linear  = run_rate * 20
    balls_bowled      = (current_over + 1) * 6

    X = pd.DataFrame([[current_over, current_runs, current_wickets, overs_remaining,
                       run_rate, wickets_remaining, batting_first, month,
                       bat_enc, bowl_enc, venue_enc, toss_enc, projected_linear, balls_bowled]],
                     columns=FEATURES)

    pred = float(model.predict(X)[0])
    return {
        'predicted_score': round(pred),
        'lower_bound': max(current_runs, round(pred - MAE)),
        'upper_bound': round(pred + MAE),
        'run_rate': round(run_rate, 2),
        'projected_linear': round(projected_linear),
        'wickets_remaining': wickets_remaining,
        'overs_remaining': overs_remaining,
    }


